def get_kr_name(ticker: str) -> str:
    """티커 → 한글명 (없으면 ticker 그대로)"""
    return TICKER_TO_KR_NAME.get(ticker, ticker)


def get_short_ticker(ticker: str) -> str:
    """티커에서 .KS/.KQ 제거 (006930.KS → 006930)"""
    return ticker.replace(".KS", "").replace(".KQ", "")


def format_ticker_with_name(ticker: str, max_name_len: int = 12) -> str:
    """
    '005930 삼성전자' 형식 출력.
    한글명이 너무 길면 잘라냄.
    """
    short = get_short_ticker(ticker)
    name = get_kr_name(ticker)
    # 매핑 없으면 short만 반환
    if name == ticker:
        return short
    if len(name) > max_name_len:
        name = name[:max_name_len] + "…"
    return f"{short} {name}"


